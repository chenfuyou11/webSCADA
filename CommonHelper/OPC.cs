        Array strClientHandles;
        Array strServerHandles;
        Array readServerHandles;
        Array writeServerHandles;
        Array writeArrayHandles;
        Array readError;
        Array writeError;
        int readTransID;
        int writeTransID;
        int readCancelID;
        int writeCancelID;
        #endregion

        #region 连接OpcServer函数
        /// <summary>
        /// 连接OpcServer函数
        /// </summary>
        /// <param name="ServerName">KepServer名称</param>
        /// <param name="ServerNode">服务器地址名</param>
        public void Connect(string ServerName, string ServerNode)
        {
            //连接选择的OPC服务器
            opcServer.Connect(ServerName, ServerNode);
            //kepGruops对象赋值 
            opcGruops = opcServer.OPCGroups;
            //kepGruops属性设置
            opcGruops.DefaultGroupDeadband = 0;
            opcGruops.DefaultGroupIsActive = true;
            opcGruops.DefaultGroupUpdateRate = 10;//10毫秒刷新一次  默认1000毫秒

            opcGruop = opcGruops.Add("group1");
            opcGruop.IsActive = true;
            opcGruop.IsSubscribed = true;
            opcGruop.UpdateRate = 250;
            opcGruop.AsyncReadComplete += opcGruop_AsyncReadComplete;


            TempIDList.Clear();
            ClientHandles.Clear();
            TempIDList.Add("0");
            ClientHandles.Add(0);
            int count = this.OPCList.Count;
            for (int i = 0; i < count; i++)
            {
                TempIDList.Add(this.OPCList[i].Tag);
                ClientHandles.Add(i + 1);
            }
            strTempIDs = (Array)TempIDList.ToArray();
            strClientHandles = (Array)ClientHandles.ToArray();
            opcItems = opcGruop.OPCItems;
            opcItems.AddItems(this.OPCList.Count, ref strTempIDs, ref strClientHandles, out strServerHandles, out iErrors);
            serverHandles.Clear();
            serverHandles.Add(0);
            for (int i = 0; i < count; i++)
            {
                serverHandles.Add(Convert.ToInt32(strServerHandles.GetValue(i + 1)));

            }
            readServerHandles = (Array)serverHandles.ToArray();
        }
        #endregion

        #region 同步写入函数
        /// <summary>
        /// 同步写入函数
        /// 写入函数
        /// 写入标签值
        /// </summary>
        /// <param name="tag"></param>
        /// <param name="value"></param>
        public void SyncWriteOpcItems(string tag, object value)
        {
            int index = GetIndex(tag);
            int[] serverHandle = new int[] { 0, Convert.ToInt32(strServerHandles.GetValue(index + 1)) };
            object[] values = new object[2];
            writeServerHandles = (Array)serverHandle;
            values[1] = value;
            writeArrayHandles = (Array)values;
            opcGruop.SyncWrite(1, ref writeServerHandles, ref writeArrayHandles, out writeError);
            //opcGruop.AsyncWrite(1, ref writeServerHandles, ref writeArrayHandles, out writeError, writeTransID, out writeCancelID);
        }
        #endregion

        #region 同步读取函数
        private Array _arrValues;
        private Array _arrErrors;
        private object _qualities, _timeStamps;
        /// <summary>
        /// 同步读取函数
        /// </summary>
        public void SyncReadOpcItems()
        {

            try
            {
                if (this.OPCList.Count > 0)
                {
                    if (opcServer != null)
                    {
                        //同步读取所有的数据
                        opcGruop.SyncRead((short)OPCDataSource.OPCDevice, this.OPCList.Count, ref readServerHandles, out _arrValues, out _arrErrors, out _qualities, out _timeStamps);
                        //解析并进行存储到OPCList中
                        for (int i = 1; i <= this.OPCList.Count; i++)
                        {
                            object value = _arrValues.GetValue(i);

                            if (value != null)
                            {
                                this.OPCList[i - 1].Value = value.ToString();
                                this.OPCList[i - 1].Time = ((DateTime)((Array)_timeStamps).GetValue(1)).ToLocalTime().ToLongTimeString();
                            }
                        }
                    }
                }
            }
            catch (Exception e)
            {
                Console.WriteLine(e);
            }

        }


        #endregion

        #region 异步写入函数
        /// <summary>
        /// 异步写入函数
        /// 写入函数
        /// 写入标签值
        /// </summary>
        /// <param name="tag">标签名,与数据库中的标签相对应</param>
        /// <param name="value">要写入的值</param>
        public void AsyncWriteOpcItems(string tag, object value)
        {
            int index = GetIndex(tag);
            int[] serverHandle = new int[] { 0, Convert.ToInt32(strServerHandles.GetValue(index + 1)) };
            object[] values = new object[2];
            writeServerHandles = (Array)serverHandle;
            values[1] = value;
            writeArrayHandles = (Array)values;
            opcGruop.AsyncWrite(1, ref writeServerHandles, ref writeArrayHandles, out writeError, writeTransID, out writeCancelID);
        }
        #endregion

        #region 异步读取函数
        /// <summary>
        /// 异步读取函数
        /// </summary>
        public void AsyncReadOpcItems()
        {
            if (this.OPCList.Count > 0)
            {
                if (opcServer != null)
                {
                    opcGruop.AsyncRead(this.OPCList.Count, ref readServerHandles, out readError, readTransID, out readCancelID);
                }
            }
        }
        #endregion

        #region 异步读取完成引发的事件函数
        /// <summary>
        /// 异步读取完成引发的事件函数
        /// </summary>
        /// <param name="transactionId"></param>
        /// <param name="numItems"></param>
        /// <param name="clientHandles"></param>
        /// <param name="itemValues"></param>
        /// <param name="qualities"></param>
        /// <param name="timeStamps"></param>
        /// <param name="errors"></param>
        public void opcGruop_AsyncReadComplete(int transactionId, int numItems, ref Array clientHandles,
            ref Array itemValues,
            ref Array qualities, ref Array timeStamps, ref Array errors)
        {
            for (int i = 1; i <= numItems; i++)
            {
                object value = itemValues.GetValue(i);

                if (value != null)
                {
                    this.OPCList[i - 1].Value = value.ToString();
                    this.OPCList[i - 1].Time = ((DateTime)timeStamps.GetValue(1)).ToLocalTime().ToLongTimeString();
                }

            }
        }
        #endregion

        #region 获取tag在数据库中的索引函数
        /// <summary>
        /// 获取tag在数据库中的索引位置
        /// </summary>
        /// <param name="tag"></param>
        /// <returns></returns>
        private static int GetIndex(string tag)
        {
            var index = 0;
            //OpcDb opcHelp = new OpcDb();

            BaseDBContext opcHelp = new BaseDBContext();

            var ls = opcHelp.OpcHelperItem.ToList();
            for (int i = 0; i < ls.Count; i++)
            {
                //找出该tag在数据库中的索引位置
                if (ls[i].EnumTag == tag)
                {
                    index = i;
                    break;
                }
            }
            return index;
        }
        #endregion

